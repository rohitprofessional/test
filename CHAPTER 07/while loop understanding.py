i = 10           # Intitialization
while(i>=0):   # Condition
    print(i,"Reverse While loop is running.")
    i = i-1     # Incrementation. This is must to stop the loop at some condition. Otherwise infinite loop.

print("Reverse While loop stops at: ", i)
