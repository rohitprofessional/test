'''Instead of using for loop to create a mathematical logic we have
some inbuilt functions in python w/c we can use to perform some of
often used mathematifal operation in a program. It saves our time 
and effort. Also it helps to increase the interpreter speed.
'''

# for this just use "import math"
import math

x = 12.1
print(math.ceil(x))

x = 12.8
print(math.floor(x))

x = -10.5
print(math.fabs(x))

x = 5
print(math.factorial(x))

x = 10.5
print(math.sqrt(x))

x = [10,20,30,40]
print(math.fsum(x))